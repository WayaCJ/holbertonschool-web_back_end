#!/usr/bin/env python3

""" Defines the sum of the floar values"""


def add(a: float, b: float) -> float:
    """
    Calculate the sum of two floating-point numbers.

    Parameters:
    a (float): The first number to add.
    b (float): The second number to add.

    Returns:
    float: The sum of a and b
    """
    return a + b
